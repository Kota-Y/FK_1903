import UIKit

extension UIColor {
    static var lookerBlack: UIColor {
        return UIColor.hex(string: "3C3C3C", alpha: 1.0)
    }
    static var lookerGray: UIColor {
        return UIColor.hex(string: "D8D8D8", alpha: 1.0)
    }
    static var lookerWhite: UIColor {
        return UIColor.hex(string: "FFFFFF", alpha: 1.0)
    }
    static var lookerBlue: UIColor {
        return UIColor.hex(string: "0091FF", alpha: 1.0)
    }
}
